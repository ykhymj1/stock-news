// 종목코드 → 회사명 역매핑 (자동 생성)
window.KR_CODE_TO_NAME = {};
Object.entries(window.KR_STOCKS).forEach(([name, code]) => {
  if (!window.KR_CODE_TO_NAME[code]) window.KR_CODE_TO_NAME[code] = name;
});

// 텍스트에서 한국 종목 추출 (모든 등록 종목 검색)
window.extractAllKRTickers = function(text) {
  const found = [];
  const seen = new Set();
  // 1. 회사명 직접 매칭 (긴 이름 우선 = 더 정확)
  const sortedNames = Object.keys(window.KR_STOCKS).sort((a, b) => b.length - a.length);
  for (const name of sortedNames) {
    if (text.includes(name)) {
      const code = window.KR_STOCKS[name];
      if (!seen.has(code)) {
        seen.add(code);
        found.push({ ticker: code, name });
      }
    }
  }
  // 2. 6자리 종목코드 직접 매칭
  const codeMatches = text.match(/\b(\d{6})\b/g);
  if (codeMatches) {
    for (const code of codeMatches) {
      if (!seen.has(code) && window.KR_CODE_TO_NAME[code]) {
        seen.add(code);
        found.push({ ticker: code, name: window.KR_CODE_TO_NAME[code] });
      }
    }
  }
  return found;
};
